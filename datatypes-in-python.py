# Python is a dynamic typed language, or in other words, the data type
# accomodates to the data stored in variables

# naming conventions
    #Case sensitive
    #can have any letter, number and underscore
    #but can't start with a number

# prints <class 'int'>
x = 1
print(type(x))

# prints <class 'float'>
y = 1.0
print(type(y))

# Boolean can hold True (1 in Int) or False (0 in Int)
# prints <class 'bool'>
t = True
print(type(t))

# string preferably are declared with single quotes, but double
# can be used too
name = 'Carlos Rodriguez'
print(type(name))
